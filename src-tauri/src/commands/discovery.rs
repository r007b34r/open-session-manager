fn infer_environment(home_dir: &std::path::Path) -> &'static str {
    if home_dir
        .to_string_lossy()
        .chars()
        .nth(1)
        .is_some_and(|character| character == ':')
    {
        "windows"
    } else {
        "linux"
    }
}
