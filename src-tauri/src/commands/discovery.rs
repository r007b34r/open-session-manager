use std::path::PathBuf;

use crate::discovery::{
    DiscoveryContext, KnownPath,
    linux::{opencode_user_config, opencode_user_data_root},
    windows::{claude_user_settings, codex_user_config},
};

pub fn discover_known_roots(context: &DiscoveryContext) -> Vec<KnownPath> {
    let native_environment = infer_environment(&context.home_dir);
    let mut roots = vec![
        KnownPath::new(
            "codex",
            "config",
            native_environment,
            codex_user_config(&context.home_dir),
        ),
        KnownPath::new(
            "claude-code",
            "config",
            native_environment,
            claude_user_settings(&context.home_dir),
        ),
        KnownPath::new(
            "opencode",
            "config",
            native_environment,
            opencode_user_config(&context.home_dir, context.xdg_config_home.as_deref()),
        ),
        KnownPath::new(
            "gemini-cli",
            "config",
            native_environment,
            context.home_dir.join(".gemini").join("settings.json"),
        ),
        KnownPath::new(
            "github-copilot-cli",
            "config",
            native_environment,
            context.home_dir.join(".copilot").join("config.json"),
        ),
        KnownPath::new(
            "qwen-cli",
            "config",
            native_environment,
            context.home_dir.join(".qwen").join("settings.json"),
        ),
        KnownPath::new(
            "factory-droid",
            "config",
            native_environment,
            context.home_dir.join(".factory").join("settings.json"),
        ),
        KnownPath::new(
            "openclaw",
            "config",
            native_environment,
            context.home_dir.join(".openclaw").join("openclaw.json"),
        ),
    ];

    if let Some(wsl_home) = &context.wsl_home_dir {
        roots.push(KnownPath::new(
            "codex",
            "config",
            "wsl",
            PathBuf::from(wsl_home).join(".codex").join("config.toml"),
        ));
        roots.push(KnownPath::new(
            "claude-code",
            "config",
            "wsl",
            PathBuf::from(wsl_home)
                .join(".claude")
                .join("settings.json"),
        ));
        roots.push(KnownPath::new(
            "opencode",
            "config",
            "wsl",
            opencode_user_config(wsl_home, None),
        ));
        roots.push(KnownPath::new(
            "gemini-cli",
            "config",
            "wsl",
            PathBuf::from(wsl_home)
                .join(".gemini")
                .join("settings.json"),
        ));
        roots.push(KnownPath::new(
            "github-copilot-cli",
            "config",
            "wsl",
            PathBuf::from(wsl_home).join(".copilot").join("config.json"),
        ));
        roots.push(KnownPath::new(
            "qwen-cli",
            "config",
            "wsl",
            PathBuf::from(wsl_home).join(".qwen").join("settings.json"),
        ));
        roots.push(KnownPath::new(
            "factory-droid",
            "config",
            "wsl",
            PathBuf::from(wsl_home)
                .join(".factory")
                .join("settings.json"),
        ));
        roots.push(KnownPath::new(
            "openclaw",
            "config",
            "wsl",
            PathBuf::from(wsl_home)
                .join(".openclaw")
                .join("openclaw.json"),
        ));
    }

    roots
}

